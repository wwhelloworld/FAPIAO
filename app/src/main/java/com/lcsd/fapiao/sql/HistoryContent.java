package com.lcsd.fapiao.sql;

public class HistoryContent {

	private String fp_dm;
	private String fp_no;
	private String fp_date;
	private String fp_amount;
	private String fp_jym;

	public HistoryContent() {
		super();
	}

	public String getFp_dm() {
		return fp_dm;
	}

	public void setFp_dm(String fp_dm) {
		this.fp_dm = fp_dm;
	}

	public String getFp_no() {
		return fp_no;
	}

	public void setFp_no(String fp_no) {
		this.fp_no = fp_no;
	}



	public String getFp_amount() {
		return fp_amount;
	}

	public void setFp_amount(String fp_amount) {
		this.fp_amount = fp_amount;
	}

	public String getFp_jym() {
		return fp_jym;
	}

	public void setFp_jym(String fp_jym) {
		this.fp_jym = fp_jym;
	}

	public String getFp_date() {
		return fp_date;
	}

	public void setFp_date(String fp_date) {
		this.fp_date = fp_date;
	}
}
